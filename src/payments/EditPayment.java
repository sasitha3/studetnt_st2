    public void getIdFromTable(Payments pay) {

        paidSID.setText(pay.getStudentID());
        recieptNo.setText(pay.getRecieptNo());
        paidAmount.setText(new Double(pay.getAmount()).toString());
        yearPaid.setSelectedItem(new Integer(pay.getYear()));
        semesterPaid.setSelectedItem(new Integer(pay.getSemester()));
        String dateValue = pay.getDate(); 
        try {
        java.util.Date dateFound = new SimpleDateFormat("yyyy-dd-MM").parse(dateValue);
         date.setDate(dateFound);
        }catch(Exception e){
            System.out.println(e);
        }
       
    }

