private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        Payments pay = new Payments();
        LogAgain log = new LogAgain();
        
        pay.setRecieptNo(recieptNo.getText());
        pay.setStudentID(paidSID.getText());
        pay.setAmount(Double.parseDouble(paidAmount.getText()));
        pay.setAdminID(log.getPssingAdminID());
        pay.setYear(Integer.parseInt(yearPaid.getSelectedItem().toString()));
        pay.setSemester(Integer.parseInt(semesterPaid.getSelectedItem().toString()));
        pay.setDate(pay.dateSetter(date.getCalendar().getTime().toString()));
        System.out.println("Line 1");
        JOptionPane.showMessageDialog(this, pay.getDate());
        if (db.editPayments(pay)) {
            JOptionPane.showMessageDialog(this, "Success Edit");
        } else {
            JOptionPane.showMessageDialog(this, "Fail");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Fields cannot keep empty");
        System.out.println(e);
    }
}//GEN-LAST:event_jButton1ActionPerformed

